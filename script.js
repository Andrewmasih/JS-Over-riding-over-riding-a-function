/*overiding example, let us update property values and methods in the derived class. there are simple overridng examples. */

function SaySomething ()  {
  console.log ("Something");
}

function SaySomething () {
  console.log("SomethingElse"); /*overriding, updating property values and methods in the derived class, the 2nd function will be excuted*/
};

SaySomething (); /*this is how to call a function, you can overide a 'function' if you declare it again after original declaration.   */




